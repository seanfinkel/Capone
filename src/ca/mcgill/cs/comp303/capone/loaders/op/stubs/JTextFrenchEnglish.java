public class JTextFrenchEnglish
{
	String fr;
	String en;

	public JTextFrenchEnglish(String fr, String en)
	{
		super();
		this.fr = fr;
		this.en = en;
	}

	public String getFr()
	{
		return fr;
	}

	public String getEn()
	{
		return en;
	}

}
